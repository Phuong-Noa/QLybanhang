    @Override
    public void moveTo(int index) {
        if (index < 0) {
            this.moveLast();
        } else if (index >= tblSanPham.getRowCount()) {
            this.moveFirst();
        } else {
            tblSanPham.clearSelection();
            tblSanPham.setRowSelectionInterval(index, index);
            this.edit();
        }
        if (index >= 0 && index < tblSanPham.getRowCount()) {
            tblSanPham.setRowSelectionInterval(index, index); // chọn dòng

            String maSP = tblSanPham.getValueAt(index, 0).toString();
            SanPham sp = dao.findById(Integer.parseInt(maSP));

            if (sp != null) {
                setForm(sp); // show thông tin lên form
            }
        }
    }

    private void setCheckedAll(boolean checked) {
        for (int i = 0; i < tblSanPham.getRowCount(); i++) {
            tblSanPham.setValueAt(checked, i, 6);
        }
    }

    @Override
    public void fillSanPhamTheoLoai() {
        int selectedRow = tblLoaiSanPham.getSelectedRow();
        if (selectedRow >= 0) {
            String maloai = loaisanpham.get(selectedRow).getMaloai();

            DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
            model.setRowCount(0);

            items = dao.findByMaLoai(maloai); // DAO đã có sẵn hàm này
            int stt = 1;
            for (SanPham item : items) {
                model.addRow(new Object[]{
                    stt++, // STT bắt đầu từ 1
                    item.getTensp(),
                    item.getDvt(),
                    item.getNuocsx(),
                    item.getGia(),
                    item.getMaloai(),
                    false
                });
            }
            this.clear(); // reset form
            // txtMasp.setText(generateMaSPTheoLoai(maloai));
        }
    }

    @Override
    public void search() {
        String keyword = txtSearch.getText().trim();
        DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
        model.setRowCount(0); // Clear bảng

        if (keyword.isEmpty()) {
            // Nếu không nhập gì → hiển thị theo loại đang chọn
            this.fillSanPhamTheoLoai();
            return;
        }

        SanPham sp = dao.findById(Integer.parseInt(keyword));
        if (sp != null) {
            items = List.of(sp);
        } else {
            items = dao.findByName(keyword);
            if (items.isEmpty()) {
                XDialog.alert("Không tìm thấy sản phẩm với mã hoặc tên: " + keyword);
                // Sau khi thông báo → load lại theo loại để bảng không trống
                this.fillSanPhamTheoLoai();
                return;
            }
        }

        for (SanPham item : items) {
            model.addRow(new Object[]{
                item.getMasp(),
                item.getTensp(),
                item.getDvt(),
                item.getNuocsx(),
                item.getGia(),
                item.getMaloai(),
                false
            });
        }
    }

