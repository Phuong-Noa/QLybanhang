    public int generateNewMaSP() {
        int rowCount = tblSanPham.getRowCount();
        int max = 0;

        for (int i = 0; i < rowCount; i++) {
            int masp = Integer.parseInt(tblSanPham.getValueAt(i, 0).toString());
            if (masp > max) {
                max = masp;
            }
        }

        return max + 1;
    }

    @Override
    public void fillLoaiSanPham() {
        DefaultComboBoxModel cboModel = (DefaultComboBoxModel) cboLoaiSanpham.getModel();
        cboModel.removeAllElements();

        DefaultTableModel tblModel = (DefaultTableModel) tblLoaiSanPham.getModel();
        tblModel.setRowCount(0);

        LoaiSanPhamDAO cdao = new LoaiSanPhamDAOImpl();
        loaisanpham = cdao.findAll();

        loaisanpham.forEach(category -> {
            cboModel.addElement(category);
            tblModel.addRow(new Object[]{category.getTenloai()});
        });

        if (!loaisanpham.isEmpty()) {
            tblLoaiSanPham.setRowSelectionInterval(0, 0);
            String maloai = loaisanpham.get(0).getMaloai();
            this.fillSanPhamTheoLoai(); // => load bảng tblSanPham theo loại
        }
    }

    @Override
    public void open() {
        this.setLocationRelativeTo(null);
        this.fillLoaiSanPham();
        this.fillSanPhamTheoLoai();
        this.clear();
    }

    @Override
    public void setForm(SanPham entity) {
        txtMasp.setText(String.valueOf(entity.getMasp()));
        txtTensp.setText(entity.getTensp());
        cboDVT.setSelectedItem(entity.getDvt());
        cboNuocSX.setSelectedItem(entity.getNuocsx());
        //txtGia.setText(String.valueOf(entity.getGia()));
        txtGia.setText(String.valueOf(entity.getGia()));
        LoaiSanPham loai = loaisanpham.get(tblLoaiSanPham.getSelectedRow());
        cboLoaiSanpham.setSelectedItem(loai);
    }

    @Override
    public SanPham getForm() {
        try {
            int maSP = Integer.parseInt(txtMasp.getText().trim());
            String tenSP = txtTensp.getText().trim();
            String donVi = cboDVT.getSelectedItem().toString();
            String quocGia = cboNuocSX.getSelectedItem().toString();
            double gia = Double.parseDouble(txtGia.getText().trim());
            String loaiSP = cboLoaiSanpham.getSelectedItem().toString();

            return SanPham.builder()
                    .masp(maSP)
                    .tensp(tenSP)
                    .dvt(donVi)
                    .nuocsx(quocGia)
                    .gia(gia)
                    .maloai(loaiSP)
                    .build();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Dữ liệu không hợp lệ!");
            return null;
        }
    }

    @Override
    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
        model.setRowCount(0);

        items = dao.findAll();

        for (SanPham item : items) {
            Object[] rowData = {
                item.getMasp(), // <-- Đây mới là mã sản phẩm đúng
                item.getTensp(),
                item.getDvt(),
                item.getNuocsx(),
                item.getGia(),
                item.getMaloai(),
                false
            };
            model.addRow(rowData);
        }
    }

    @Override
    public void edit() {
        SanPham entity = items.get(tblSanPham.getSelectedRow());
        this.setForm(entity);
        this.setEditable(true);
    }

    public void create() {
        if (!validateForm()) {
            return; // Dừng lại nếu dữ liệu không hợp lệ
        }

        SanPham entity = this.getForm();
        if (entity == null) {
            JOptionPane.showMessageDialog(this, "Lỗi khi lấy dữ liệu từ form.");
            return;
        }

        try {
            dao.create(entity);
            JOptionPane.showMessageDialog(this, "Thêm sản phẩm thành công!");
            this.fillSanPhamTheoLoai();  // Làm mới bảng
            this.clear();            // Xóa form
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi thêm sản phẩm: " + e.getMessage());
        }

    }

    @Override
    public void update() {
        try {
            SanPham sp = getForm();
            dao.update(sp);
            fillToTable();
            JOptionPane.showMessageDialog(this, "Cập nhật sản phẩm thành công!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi cập nhật: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

    @Override
    public void delete() {
        int rowIndex = tblSanPham.getSelectedRow();
        if (rowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm trong bảng để xóa!");
            return;
        }

        String id = (String) tblSanPham.getValueAt(rowIndex, 0); // Lấy mã sản phẩm từ bảng

        if (!XDialog.confirm("Bạn thực sự muốn xóa sản phẩm này?")) {
            return;
        }

        try {
            dao.deleteById(Integer.parseInt(id)); // Xóa trong CSDL

            // Xóa dòng khỏi bảng
            DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
            model.removeRow(rowIndex);

            this.clear(); // Xóa form nhập liệu
            JOptionPane.showMessageDialog(this, "Xóa sản phẩm thành công!");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi xóa sản phẩm: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

    @Override
    public void clear() {
        SanPham sp = new SanPham();

        // Lấy mã loại hiện tại từ combobox
        Object selectedItem = cboLoaiSanpham.getSelectedItem();
        if (selectedItem instanceof LoaiSanPham) {
            String maLoai = ((LoaiSanPham) selectedItem).getMaloai();
            sp.setMasp(generateNewMaSP());
            sp.setMaloai(maLoai); // Gán lại mã loại
        }

        this.setForm(sp);
        this.setEditable(true);
    }

