    @Override
    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblLSP.getModel();
        model.setRowCount(0);

        items = dao.findAll();
        items.forEach(item -> {
            Object[] rowData = {
                item.getMaloai(),
                item.getTenloai(),
                false
            };
            model.addRow(rowData);
        });
    }

