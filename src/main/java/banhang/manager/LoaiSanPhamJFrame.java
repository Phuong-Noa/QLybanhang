    @Override
    public int generateNewMaLoai() {
        int rowCount = tblLSP.getRowCount();
        int max = 0;

        for (int i = 0; i < rowCount; i++) {
            int maloai = Integer.parseInt(tblLSP.getValueAt(i, 0).toString());
            if (maloai > max) {
                max = maloai;
            }
        }

        return max + 1;
    }

