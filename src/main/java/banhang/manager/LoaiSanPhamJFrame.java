    @Override
    public void search() {
        String keyword = txtSearch.getText().trim();

        if (keyword.isEmpty()) {
            XDialog.alert("Vui l√≤ng nh·∫≠p m√£ ho·∫∑c t√™n ƒë·ªÉ t√¨m ki·∫øm.");
            return;
        }

        // T√¨m theo m√£ lo·∫°i tr∆∞·ªõc
        int id = Integer.parseInt(keyword);
        LoaiSanPham loai = dao.findById(id);
        if (loai != null) {
            items = List.of(loai);
        } else {
            items = dao.findByName(keyword);
            if (items.isEmpty()) {
                XDialog.alert("Kh√¥ng t√¨m th·∫•y lo·∫°i s·∫£n ph·∫©m v·ªõi m√£ ho·∫∑c t√™n: " + keyword);
                return;
            }
        }
        DefaultTableModel model = (DefaultTableModel) tblLSP.getModel();
        model.setRowCount(0); // Clear b·∫£ng 
        items.forEach(item -> {
            Object[] rowData = {
                item.getMaloai(),
                item.getTenloai(),
                false
            };
            model.addRow(rowData);
        });
    }   

