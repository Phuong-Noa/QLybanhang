    @Override
    public void login() {
        String username = txtUsername.getText();
        String password = new String(pfPassword.getPassword());  
        
        if (username.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập tên đăng nhập!", "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập mật khẩu đăng nhập!", "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        UserDAO dao = new UserDAOImpl();
        User user = dao.findById(username);
        if (user == null) {
            JOptionPane.showMessageDialog(null, "Sai tên đăng nhập!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            //XDialog.alert("❌ Sai tên đăng nhập!");
        } else if (!password.equals(user.getMatkhau())) {
            JOptionPane.showMessageDialog(null, "Sai mật khẩu đăng nhập!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            //XDialog.alert("❌ Sai mật khẩu đăng nhập!");
        } else if (!user.isTrangthai()) {
            JOptionPane.showMessageDialog(null, "Tài khoản của bạn đã bị vô hiệu hóa!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            //XDialog.alert("🚫 Tài khoản của bạn đã bị vô hiệu hóa!");
        } else {
            XAuth.user = user;
            
            String role = user.isChucvu()? "Quản lý" : "Nhân viên";
            XDialog.alert("Đăng nhập thành công với quyền " + role + "!");
            this.dispose();
            new BanHangJFrame().setVisible(true);
        }
    }

    @Override
    public void showpassword() {
        if (chkShowPassword.isSelected()) {
            pfPassword.setEchoChar((char) 0); // Hiện mật khẩu
        } else {
            pfPassword.setEchoChar('*'); // Ẩn mật khẩu
        }
    }
