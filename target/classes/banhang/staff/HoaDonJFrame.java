    @Override
    public void moveTo(int index) {
        if (index < 0) {
            this.moveLast();
        } else if (index >= tblHOADON.getRowCount()) {
            this.moveFirst();
        } else {
            tblHOADON.clearSelection();
            tblHOADON.setRowSelectionInterval(index, index);
            this.edit();
        }
    }

    @Override
    public int generateNewSOHD() {
        int rowCount = tblHOADON.getRowCount();
        int max = 0;

        for (int i = 0; i < rowCount; i++) {
            int makh = Integer.parseInt(tblHOADON.getValueAt(i, 0).toString());
            if (makh > max) {
                max = makh;
            }
        }

        return max + 1;
    }

