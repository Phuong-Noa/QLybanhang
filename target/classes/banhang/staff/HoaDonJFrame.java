    @Override
    public void search() {
        String keyword = txtSearch.getText().trim();

        if (keyword.isEmpty()) {
            XDialog.alert("Vui l√≤ng nh·∫≠p s·ªë ho√° ƒë∆°n ho·∫∑c m√£ nh√¢n vi√™n ƒë·ªÉ t√¨m ki·∫øm.");
            return;
        }

        try {
            int id = Integer.parseInt(keyword);
            HoaDon hd = dao.findById(id);
            if (hd != null) {
                items = List.of(hd);
            } else {
                items = dao.findByMaNV(id);
            }
        } catch (NumberFormatException e) {
            XDialog.alert("Vui l√≤ng nh·∫≠p ƒë√∫ng ƒë·ªãnh d·∫°ng s·ªë cho s·ªë ho√° ƒë∆°n ho·∫∑c m√£ nh√¢n vi√™n.");
            return;
        }

        if (items == null || items.isEmpty()) {
            XDialog.alert("Kh√¥ng t√¨m th·∫•y ho√° ƒë∆°n v·ªõi s·ªë ho√° ƒë∆°n ho·∫∑c m√£ nh√¢n vi√™n: " + keyword);
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblHOADON.getModel();
        model.setRowCount(0); // Xo√° d·ªØ li·ªáu c≈©
        items.forEach(item -> {
            Object[] rowData = {
                item.getSohd(),
                item.getManv(),
                item.getNghd(),
                false
            };
            model.addRow(rowData);
        });
    }

