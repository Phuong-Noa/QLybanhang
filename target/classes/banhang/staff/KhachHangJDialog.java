    @Override
    public void search() {
        String keyword = txtSearch.getText().trim();

        if (keyword.isEmpty()) {
            items = dao.findAll();
        } else {
            try {
                int id = Integer.parseInt(keyword);
                KhachHang kh = dao.findById(id);
                if (kh != null) {
                    items = List.of(kh);
                } else {
                    items = dao.findByName(keyword);
                }
            } catch (NumberFormatException e) {
                // N·∫øu kh√¥ng ph·∫£i s·ªë => t√¨m theo t√™n
                items = dao.findByName(keyword);
            }

            if (items == null || items.isEmpty()) {
                XDialog.alert("Kh√¥ng t√¨m th·∫•y nh√¢n vi√™n v·ªõi m√£ ho·∫∑c t√™n: " + keyword);
                return;
            }
        }

        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0); // Xo√° d·ªØ li·ªáu c≈©
        items.forEach(item -> {
            Object[] rowData = {
                item.getMakh(),
                item.getHoten(),
                item.getDchi(),
                item.getSodt(),
                item.getNgsinh(),
                item.getTongtien(),
                false
            };
            model.addRow(rowData);
        });
    }

    @Override
    public void open() {
        this.setLocationRelativeTo(null);
        this.fillToTable();
        this.clear();
        txtMaKH.setEditable(false);
    }

    @Override
    public void setForm(KhachHang entity) {
        txtMaKH.setText(String.valueOf(entity.getMakh()));
        txtHoTen.setText(entity.getHoten());
        txtDiaChi.setText(entity.getDchi());
        txtSDT.setText(entity.getSodt());
        txtNgaySinh.setText(entity.getNgsinh() == null ? "" : XDate.format(entity.getNgsinh()));
        //txtNgaySinh.setText(XDate.format(entity.getNgsinh()));
        txtTongTien.setText(String.valueOf(entity.getTongtien()));
    }

    @Override
    public KhachHang getForm() {
        KhachHang entity = new KhachHang();
        if (!txtMaKH.getText().isEmpty()) {
            // Ch·ªâ set manv n·∫øu ƒëang trong ch·∫ø ƒë·ªô c·∫≠p nh·∫≠t (update)
            entity.setMakh(Integer.parseInt(txtMaKH.getText()));
        }
        entity.setHoten(txtHoTen.getText());
        entity.setDchi(txtDiaChi.getText());
        entity.setSodt(txtSDT.getText());
        entity.setNgsinh(XDate.parse(txtNgaySinh.getText()));
        entity.setTongtien(Double.parseDouble(txtTongTien.getText()));
        return entity;
    }

    @Override
    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0);

        items = dao.findAll();
        items.forEach(item -> {
            Object[] rowData = {
                item.getMakh(),
                item.getHoten(),
                item.getDchi(),
                item.getSodt(),
                XDate.format(item.getNgsinh()),
                item.getTongtien(),
                false
            };
            model.addRow(rowData);
        });
    }

    @Override
    public void edit() {
        KhachHang entity = items.get(tblKhachHang.getSelectedRow());
        this.setForm(entity);
        this.setEditable(true);
    }

    @Override
    public void create() {
        KhachHang entity = this.getForm();
        dao.create(entity);
        this.fillToTable();
        this.clear();
    }

    @Override
    public void update() {
        KhachHang entity = this.getForm();
        dao.update(entity);
        this.fillToTable();
    }

    @Override
    public void delete() {
        if (XDialog.confirm("B·∫°n th·ª±c s·ª± mu·ªën x√≥a?")) {
            int id = Integer.parseInt(txtMaKH.getText());
            dao.deleteById(id);
            this.fillToTable();
            this.clear();
        }
    }

    @Override
    public void clear() {
        this.setForm(new KhachHang());
        this.setEditable(false);
        txtMaKH.setText("");
    }

    @Override
    public void setEditable(boolean editable) {
        txtMaKH.setEnabled(!editable);
        btnCreate.setEnabled(!editable);
        btnUpdate.setEnabled(editable);
        btnDelete.setEnabled(editable);

        int rowCount = tblKhachHang.getRowCount();
        btnMoveFirst.setEnabled(editable && rowCount > 0);
        btnMovePrevious.setEnabled(editable && rowCount > 0);
        btnMoveNext.setEnabled(editable && rowCount > 0);
        btnMoveLast.setEnabled(editable && rowCount > 0);
    }

    @Override
    public void checkAll() {
        this.setCheckedAll(true);
    }

    @Override
    public void uncheckAll() {
        this.setCheckedAll(false);
    }

    @Override
    public void deleteCheckedItems() {
        if (XDialog.confirm("B·∫°n th·ª±c s·ª± mu·ªën x√≥a c√°c m·ª•c ch·ªçn?")) {
            for (int i = 0; i < tblKhachHang.getRowCount(); i++) {
                if ((Boolean) tblKhachHang.getValueAt(i, 6)) {
                    dao.deleteById(items.get(i).getMakh());
                }
            }
            this.fillToTable();
        }
    }

    @Override
    public void moveFirst() {
this.moveTo(0);    }

    @Override
    public void movePrevious() {
        this.moveTo(tblKhachHang.getSelectedRow() - 1);
    }

    @Override
    public void moveNext() {
        this.moveTo(tblKhachHang.getSelectedRow() + 1);
    }

    @Override
    public void moveLast() {
        this.moveTo(tblKhachHang.getRowCount() - 1);
    }

    @Override
    public void moveTo(int index) {
        if (index < 0) {
            this.moveLast();
        } else if (index >= tblKhachHang.getRowCount()) {
            this.moveFirst();
        } else {
            tblKhachHang.clearSelection();
            tblKhachHang.setRowSelectionInterval(index, index);
            this.edit();
        }
    }

    private void setCheckedAll(boolean checked) {
        for (int i = 0; i < tblKhachHang.getRowCount(); i++) {
            tblKhachHang.setValueAt(checked, i, 6);
        }
    }

    @Override
    public void validateSDT() {
        txtSDT.addKeyListener(new java.awt.event.KeyAdapter() {
            @Override
            public void keyTyped(java.awt.event.KeyEvent evt) {
                char c = evt.getKeyChar();
                if (!Character.isDigit(c) && !Character.isISOControl(c)) {
                    evt.consume(); // kh√¥ng cho nh·∫≠p ch·ªØ/k√Ω t·ª±
                }
            }
        });
    }
