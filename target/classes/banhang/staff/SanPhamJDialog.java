    @Override
    public void moveTo(int index) {
        if (index < 0) {
            this.moveLast();
        } else if (index >= tblSanPham.getRowCount()) {
            this.moveFirst();
        } else {
            tblSanPham.clearSelection();
            tblSanPham.setRowSelectionInterval(index, index);
            this.edit();
        }
    }

    private void setCheckedAll(boolean checked) {
        for (int i = 0; i < tblSanPham.getRowCount(); i++) {
            tblSanPham.setValueAt(checked, i, 6);
        }
    }

    @Override
    public void fillSanPhamTheoLoai() {
        int selectedRow = tblLoaiSanPham.getSelectedRow();
        if (selectedRow >= 0) {
            String maloai = loaisanpham.get(selectedRow).getMaloai();

            DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
            model.setRowCount(0);

            items = dao.findByMaLoai(maloai); // DAO ƒë√£ c√≥ s·∫µn h√†m n√†y
            for (SanPham item : items) {
                model.addRow(new Object[]{
                    item.getMasp(),
                    item.getTensp(),
                    item.getDvt(),
                    item.getNuocsx(),
                    item.getGia(),
                    item.getMaloai(),
                    false
                });
            }
            this.clear(); // reset form
        }
    }

    @Override
    public void search() {
        String keyword = txtSearch.getText().trim();
        DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
        model.setRowCount(0); // Clear b·∫£ng

        if (keyword.isEmpty()) {
            // N·∫øu kh√¥ng nh·∫≠p g√¨ ‚Üí hi·ªÉn th·ªã theo lo·∫°i ƒëang ch·ªçn
            this.fillSanPhamTheoLoai();
            return;
        }

        SanPham sp = dao.findById(keyword);
        if (sp != null) {
            items = List.of(sp);
        } else {
            items = dao.findByName(keyword);
            if (items.isEmpty()) {
                XDialog.alert("Kh√¥ng t√¨m th·∫•y s·∫£n ph·∫©m v·ªõi m√£ ho·∫∑c t√™n: " + keyword);
                // Sau khi th√¥ng b√°o ‚Üí load l·∫°i theo lo·∫°i ƒë·ªÉ b·∫£ng kh√¥ng tr·ªëng
                this.fillSanPhamTheoLoai();
                return;
            }
        }

        for (SanPham item : items) {
            model.addRow(new Object[]{
                item.getMasp(),
                item.getTensp(),
                item.getDvt(),
                item.getNuocsx(),
                item.getGia(),
                item.getMaloai(),
                false
            });
        }
    }
