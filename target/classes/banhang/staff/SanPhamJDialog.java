    @Override
    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
        model.setRowCount(0);

        items = dao.findAll();
        items.forEach(item -> {
            Object[] rowData = {
                item.getMasp(),
                item.getTensp(),
                item.getDvt(),
                item.getNuocsx(),
                item.getGia(),
                item.getMaloai(),
                false
            };
            model.addRow(rowData);
        });
    }

