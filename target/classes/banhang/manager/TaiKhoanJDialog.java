    TaiKhoanDAO dao = new TaiKhoanDAOImpl();
    List<TaiKhoan> items = List.of();

    @Override
    public void search() {
        String keyword = txtSearch.getText().trim();

        if (keyword.isEmpty()) {
            // Nếu ô tìm kiếm trống thì load tất cả
            items = dao.findAll();
        } else {
            // Tìm theo Username trước
            TaiKhoan tk = dao.findById(keyword);
            if (tk != null) {
                items = List.of(tk);
            } else {
                // Nếu không có Username thì tìm theo Fullname hoặc MANV
                items = dao.findByName(keyword);
                if (items.isEmpty()) {
                    XDialog.alert("Không tìm thấy tài khoản với mã hoặc tên: " + keyword);
                    return;
                }
            }
        }

        DefaultTableModel model = (DefaultTableModel) tblUser.getModel();
        model.setRowCount(0); // Clear bảng

        for (TaiKhoan item : items) {
            Object[] row = {
                item.getUsername(),
                item.getPassword(),
                item.isEnabled(),
                item.getFullname(),
                item.isManager(),
                item.getManv(),
                false
            };
            model.addRow(row);
        }
    }

    @Override
    public void open() {
        this.setLocationRelativeTo(null);
        this.fillToTable();
        this.clear();
    }

    @Override
    public void setForm(TaiKhoan entity) {
        txtUsername.setText(entity.getUsername());
        txtPassword.setText(entity.getPassword());
        // Gán trạng thái làm việc
        rdoON.setSelected(entity.isEnabled());
        rdoOFF.setSelected(!entity.isEnabled());
        txtFullname.setText(entity.getFullname());
        // Gán vai trò
        rdoManager.setSelected(entity.isManager());
        rdoStaff.setSelected(!entity.isManager());
        // Nếu manv == 0 thì để trống
        //txtMaNV.setText(entity.getManv() == 0 ? "" : String.valueOf(entity.getManv()));
        txtMaNV.setText(String.valueOf(entity.getManv()));
    }

    @Override
    public TaiKhoan getForm() {
        TaiKhoan entity = new TaiKhoan();
        entity.setUsername(txtUsername.getText());
        entity.setPassword(txtPassword.getText());
        entity.setEnabled(rdoON.isSelected());
        entity.setFullname(txtFullname.getText());
        entity.setManager(rdoManager.isSelected());
        entity.setManv(Integer.parseInt(txtMaNV.getText()));
        return entity;
    }

    @Override
    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblUser.getModel();
        model.setRowCount(0);

        items = dao.findAll();
        items.forEach(item -> {
            Object[] rowData = {
                item.getUsername(),
                item.getPassword(),
                item.isEnabled(),
                item.getFullname(),
                item.isManager(),
                item.getManv(),
                false
            };
            model.addRow(rowData);
        });
    }

    @Override
    public void edit() {
        TaiKhoan entity = items.get(tblUser.getSelectedRow());
        this.setForm(entity);
        this.setEditable(true);
    }

    @Override
    public void create() {
        TaiKhoan entity = this.getForm();
        dao.create(entity);
        this.fillToTable();
        this.clear();
    }

    @Override
    public void update() {
        TaiKhoan entity = this.getForm();
        dao.update(entity);
        this.fillToTable();
    }

    @Override
    public void delete() {
        if (XDialog.confirm("Bạn thực sự muốn xóa?")) {
            String id = txtUsername.getText();
            dao.deleteById(id);
            this.fillToTable();
            this.clear();
        }
    }

    @Override
    public void clear() {
        this.setForm(new TaiKhoan());
        this.setEditable(false);
        txtMaNV.setText(""); // Chặn giá trị mặc định 0
    }

    @Override
    public void setEditable(boolean editable) {
        txtUsername.setEnabled(!editable);
        btnCreate.setEnabled(!editable);
        btnUpdate.setEnabled(editable);
        btnDelete.setEnabled(editable);

        int rowCount = tblUser.getRowCount();
        btnMoveFirst.setEnabled(editable && rowCount > 0);
        btnMovePrevious.setEnabled(editable && rowCount > 0);
        btnMoveNext.setEnabled(editable && rowCount > 0);
        btnMoveLast.setEnabled(editable && rowCount > 0);
    }

    @Override
    public void checkAll() {
        this.setCheckedAll(true);
    }

    @Override
    public void uncheckAll() {
        this.setCheckedAll(false);
    }

    @Override
    public void deleteCheckedItems() {
        if (XDialog.confirm("Bạn thực sự muốn xóa các mục chọn?")) {
            for (int i = 0; i < tblUser.getRowCount(); i++) {
                if ((Boolean) tblUser.getValueAt(i, 6)) {
                    dao.deleteById(items.get(i).getUsername());
                }
            }
            this.fillToTable();
        }
    }

    @Override
    public void moveFirst() {
        this.moveTo(0);
    }

    @Override
    public void movePrevious() {
        this.moveTo(tblUser.getSelectedRow() - 1);
    }

    @Override
    public void moveNext() {
        this.moveTo(tblUser.getSelectedRow() + 1);
    }

    @Override
    public void moveLast() {
        this.moveTo(tblUser.getRowCount() - 1);
    }

    @Override
    public void moveTo(int index) {
        if (index < 0) {
            this.moveLast();
        } else if (index >= tblUser.getRowCount()) {
            this.moveFirst();
        } else {
            tblUser.clearSelection();
            tblUser.setRowSelectionInterval(index, index);
            this.edit();
        }
    }

    private void setCheckedAll(boolean checked) {
        for (int i = 0; i < tblUser.getRowCount(); i++) {
            tblUser.setValueAt(checked, i, 6);
        }
    }

    @Override
    public void validateMANV() {
        txtMaNV.addKeyListener(new java.awt.event.KeyAdapter() {
            @Override
            public void keyTyped(java.awt.event.KeyEvent evt) {
                char c = evt.getKeyChar();
                if (!Character.isDigit(c) && !Character.isISOControl(c)) {
                    evt.consume();
                }
            }
        });
    }
