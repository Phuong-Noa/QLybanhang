    @Override
    public void search() {
        String keyword = txtSearch.getText().trim();

        if (keyword.isEmpty()) {
            XDialog.alert("Vui l√≤ng nh·∫≠p username ho·∫∑c t√™n ƒë·ªÉ t√¨m ki·∫øm.");
            return;
        }

        // T√¨m theo Username tr∆∞·ªõc
        TaiKhoan tk = dao.findById(keyword);
        if (tk != null) {
            items = List.of(tk);
        } else {
            // N·∫øu kh√¥ng c√≥ Username th√¨ t√¨m theo Fullname ho·∫∑c MANV
            items = dao.findByName(keyword);
            if (items.isEmpty()) {
                XDialog.alert("Kh√¥ng t√¨m th·∫•y t√†i kho·∫£n v·ªõi m√£ ho·∫∑c t√™n: " + keyword);
                return;
            }
        }

        DefaultTableModel model = (DefaultTableModel) tblUser.getModel();
        model.setRowCount(0); // Clear b·∫£ng

        for (TaiKhoan item : items) {
            Object[] row = {
                item.getTendangnhap(),
                item.getMatkhau(),
                item.isTrangthai(),
                item.getHoten(),
                item.isChucvu(),
                item.getManv(),
                false
            };
            model.addRow(row);
        }
    }

