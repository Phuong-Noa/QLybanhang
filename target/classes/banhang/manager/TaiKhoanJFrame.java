    @Override
    public void deleteCheckedItems() {
        boolean hasChecked = false;
        for (int i = 0; i < tblUser.getRowCount(); i++) {
            if ((Boolean) tblUser.getValueAt(i, 6)) {
                hasChecked = true;
                break;
            }
        }

        if (!hasChecked) {
            XDialog.alert("Vui l√≤ng ch·ªçn √≠t nh·∫•t m·ªôt t√†i kho·∫£n ƒë·ªÉ xo√°.");
            return;
        }

        if (XDialog.confirm("B·∫°n th·ª±c s·ª± mu·ªën x√≥a c√°c m·ª•c ƒë√£ ch·ªçn?")) {
            int deletedCount = 0;
            for (int i = 0; i < tblUser.getRowCount(); i++) {
                if ((Boolean) tblUser.getValueAt(i, 6)) {
                    dao.deleteById(items.get(i).getTendangnhap());
                    deletedCount++;
                }
            }
            this.fillToTable();
            XDialog.alert("ƒê√£ xo√° " + deletedCount + " t√†i kho·∫£n ƒë∆∞·ª£c ch·ªçn.");
        }
    }


